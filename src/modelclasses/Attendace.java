	package modelclasses;
	
	
	
	public class Attendace {
		private String date;
		private int status;
		public Attendace() {
			super();
			
		}
		public String getDate() {
			return date;
		}
		public void setDate(String string) {
			this.date = string;
		}
		public int getStatus() {
			return status;
		}
		public void setStatus(int status) {
			this.status = status;
		}
	
		
		
	}
